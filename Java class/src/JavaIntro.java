public class JavaIntro {
    public static void main(String[] args)
    {
        byte myMaxByteExample = Byte.MAX_VALUE;
        byte myMinByteExample = Byte.MIN_VALUE;
        int myMaxIntExample = Integer.MAX_VALUE;
        int myMinIntExample = Integer.MIN_VALUE;
        long myMaxLongExample = Long.MAX_VALUE;
        long myMinLongExample = Long.MIN_VALUE;
        float myMaxFloatExample = Float.MAX_VALUE;
        float myMinFloatExample = Float.MIN_VALUE;
        double myMaxDoubleExample = Double.MAX_VALUE;
        double myMinDoubleExample = Double.MIN_VALUE;


        System.out.println("The Maximum value byte can store is" + myMaxByteExample);
        System.out.println("The Minimum value byte can store is" + myMinByteExample);
        System.out.println("The Maximum value int can store is" + myMaxIntExample);
        System.out.println("The Minimum value int can store is" + myMinIntExample);
        System.out.println("The Maximum value long can store is" + myMaxLongExample);
        System.out.println("The Minimum value long can store is" + myMinLongExample);
        System.out.println("The Maximum value float can store is" + myMaxFloatExample);
        System.out.println("The Minimum value float can store is" + myMinFloatExample);
        System.out.println("The Maximum value double can store is" + myMaxDoubleExample);
        System.out.println("The Minimum value byte can store is" + myMinDoubleExample);

    }

}
